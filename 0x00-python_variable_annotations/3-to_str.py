#!/usr/bin/env python3
"""
Module 3-to_str
Contains a function to convert a float to its string representation
"""


def to_str(n: float) -> str:
    """
    Return the string representation of a float number.

    Args:
    n (float): The float number to be converted to string.

    Returns:
    str: The string representation of the float number.
    """
    return str(n)
