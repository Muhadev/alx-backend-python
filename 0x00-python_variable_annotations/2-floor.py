#!/usr/bin/env python3
"""
Module 2-floor
Contains a function to return the floor of a float
"""

import math


def floor(n: float) -> int:
    """
    Return the floor of a float number.

    Args:
    n (float): The float number to be floored.

    Returns:
    int: The floor of the float number.
    """
    return math.floor(n)
